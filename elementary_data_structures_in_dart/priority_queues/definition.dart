/* Priority Queues :-
-------------------
- Queues are simply lists that maintain the order of elements using first-in-first-out
  (FIFO) ordering. A priority queue is another version of a queue in which elements are
  dequeued in priority order instead of FIFO order.

A priority queue can be either of these two:
--------------------------------------------
1. Max-priority, in which the element at the front is always the largest.
2. Min-priority, in which the element at the front is always the smallest.
*/